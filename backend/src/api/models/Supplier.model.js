import { model, Schema } from "mongoose";

const SupplierSchema = new Schema(
    {
        id: {
            type: Schema.Types.ObjectId,
        },
        name: {
            type: String,
            required: true,
        },
        address: {
            type: String,
            required: true,
        },
        phone: {
            type: String,
            required: true,
        },
        email: {
            type: String,
            required: true,
        },
    },
    {
        timestamps: true,
    }
);

export default model("Supplier", SupplierSchema);